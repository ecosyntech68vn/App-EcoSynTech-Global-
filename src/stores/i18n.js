export function t(key, lang) {
  const map = LANG[lang] || LANG.vi;
  return map[key] || key;
}

export function getLang(l) {
  return LANG[l] || LANG.vi;
}

export const langStore = {
  async load() {
    return (await get(KEY)) || 'vi';
  },
  async save(lang) {
    await set(KEY, lang);
  },
  getLanguages() {
    return Object.entries(LANG).map(([k, v]) => ({ id: k, name: v._name }));
  }
};
